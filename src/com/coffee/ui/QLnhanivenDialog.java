 private void init() {
        this.setLocationRelativeTo(null);
       //this.setIconImage(XImage.getAppIcon());
       
       this.fillToTable();
     
       this.row = -1;
    }
    
    void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tbldsnhanvien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = nvdao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {
                   nv.getMaNV()
                        ,nv.getTenNV(),
                        nv.getGioiTinh()?"Nam":"Nữ",
                        nv.getDiaChi(),nv.getsDT(),
                        nv.getEmail(),
                       XDate.toString(nv.getNgayVaoLam(),"dd-MM-yyyy"),
                        nv.getVaiTro()?"Trưởng Phòng":"Nhân Viên",
                        nv.getMatKhau(),
                        nv.getHinh()
                };
                model.addRow(row);
            }
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
public boolean check() {
    List<NhanVien> list = nvdao.selectAll();
        for (NhanVien nv : list) {
            if (txtmanv.getText().equalsIgnoreCase(nv.getMaNV())) {
                 MsgBox.alert(this, "Mã nhanvien đã bị trùng!");
                return false;
            } else if(txtmanv.getText().equals("")|| txtmatkhau.getText().equals("")||txttennv.getText().equals("")||txtmail.getText().equals("")||txtsdt.getText().equals("")){
                 MsgBox.alert(this, "Mời bạn nhạp thông tin của nhanvien!");
                 return false;
            }
        }
        return true;

    }
    void insert(){
       NhanVien nv  = getForm();
        String matkhau2 =new String(txtxacnhanmk.getPassword());
        if(!matkhau2.equals(nv.getMatKhau())){
         MsgBox.alert(this,"Xác nhân mật khẩu không đúng!!");
        } else{          
        try {
            if(check()){
            nvdao.insert(nv);
            this.fillToTable();
            this.add();
            MsgBox.alert(this, "Thêm mới thành công!");
            }
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }

    }
    }

    void update(){
        NhanVien nv  = getForm();
        try {
            nvdao.update(nv);
            this.fillToTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên này!");
        }
        else{
            if(MsgBox.confirm(this, "Bạn có muốn xóa hay không?")){
                String manv = txtmanv.getText();
                try {
                    nvdao.delete(manv);
                    this.fillToTable();
                    this.add();
                    MsgBox.alert(this, "Xóa thành công!");
                } 
                catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    void add(){
        this.setForm(new NhanVien());
        this.row = 1;
        
    }

    void edit() {
        String manv = (String) tbldsnhanvien.getValueAt(this.row, 0);
         NhanVien nv = nvdao.selectById(manv);
         this.setForm(nv);
        
         
    }

    void setForm(NhanVien nv){
        txtmanv.setText(nv.getMaNV());
        txtmatkhau.setText(nv.getMatKhau());
        txtxacnhanmk.setText(nv.getMatKhau());
        txttennv.setText(nv.getTenNV());
        String vaitro = tbldsnhanvien.getValueAt(row,7).toString();
         if(vaitro.equals("Trưởng Phòng")){
            rdotruongphong.setSelected(true);
         }else{
             
             rdonhanvien.setSelected(true);
         }
      
        String gioitinh = tbldsnhanvien.getValueAt(row,2).toString();
         if(gioitinh.equals("Nam")){
            rdonam.setSelected(true);
         }else{
             
             rdonu.setSelected(true);
         }
       
         txtsdt.setText(nv.getsDT());
         txtngayvaolam.setText(XDate.toString(nv.getNgayVaoLam(),"dd-MM-yyyy"));
         txtmail.setText(nv.getEmail());
         txtdiachi.setText(nv.getDiaChi());
        if(nv.getHinh() != null){
            lblhinhanh.setToolTipText(nv.getHinh());
            lblhinhanh.setIcon(XImage.read(nv.getHinh()));
        }
    }
    NhanVien getForm(){
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtmanv.getText());
        nv.setMatKhau(txtmatkhau.getText());
        nv.setTenNV(txttennv.getText());
        nv.setVaiTro(rdotruongphong.isSelected());
        nv.setGioiTinh(rdonam.isSelected());
        nv.setsDT(txtsdt.getText());
        nv.setNgayVaoLam(XDate.toDate(txtngayvaolam.getText(),"dd-MM-yyyy"));
        nv.setEmail(txtmail.getText());
        nv.setDiaChi(txtdiachi.getText());
        nv.setHinh(lblhinhanh.getToolTipText());
        return nv;
    }   
    
     void chonAnh() {
        if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            XImage.save(file); 
            ImageIcon icon = XImage.read(file.getName()); 
           lblhinhanh.setIcon(icon);
            lblhinhanh.setToolTipText(file.getName()); 
        }
    }
