    private void insert() {
        NhanVien nv = getForm();
        String confirm = new String(txtxacnhanmk.getPassword());
        if (confirm.equals(nv.getMatKhau())) {
            nvdao.insert(nv);
            this.fillToTable();
            this.clearForm();
            MsgBox.alert(this, "Lưu nhân viên thành công");
        }
    }

    private void update() {
        NhanVien nv = getForm();
        String confirm = new String(txtxacnhanmk.getPassword());
        if (confirm.equals(nv.getMatKhau())) {
            nvdao.update(nv);
            this.fillToTable();
            this.clearForm();
            MsgBox.alert(this, "Cập nhật nhân viên thành công");
        }
    }

